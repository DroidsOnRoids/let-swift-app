import UIKit

extension UIColor {
    static var swiftOrange: UIColor {
        return #colorLiteral(red: 1, green: 0.7529411765, blue: 0.2156862745, alpha: 1)
    }
    
    static var lightBlueGrey: UIColor {
        return #colorLiteral(red: 0.8156862745, green: 0.8549019608, blue: 0.9098039216, alpha: 1)
    }
    
    static var coolGrey: UIColor {
        return #colorLiteral(red: 0.6196078431, green: 0.6392156863, blue: 0.6705882353, alpha: 1)
    }
}
